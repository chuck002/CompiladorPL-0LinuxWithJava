    public void cargarInt(long i) {
        if (i < 0) {
            i = i + 4294967296L; // 4.294.967.296
        }
        memoria[topeMemoria] = (byte) (i % 256); // 00 00 00 0F = 15
        memoria[topeMemoria + 1] = (byte) (i / 256 % 256); // 00 00 12 0F = 18
        memoria[topeMemoria + 2] = (byte) (i / 65536 % 256); // 00 41 12 0F = 65
        memoria[topeMemoria + 3] = (byte) (i / 16777216);

        topeMemoria = topeMemoria + 4;
    }

